-- Relatórios utilizando classificação de Dados

-- Relatório 1: Classificação de veículos por marca
SELECT fabricante, COUNT(*) AS quantidade_veiculos
FROM tbl_veiculos
GROUP BY fabricante
ORDER BY quantidade_veiculos DESC;

-- Relatório 2: Classificação de serviços por descrição
SELECT descricao, COUNT(*) AS quantidade_servicos
FROM tbl_servicos
GROUP BY descricao
ORDER BY quantidade_servicos DESC;



-- Relatórios utilizando alguma função do tipo numérica simples

-- Relatório 1: Média dos preços dos serviços
SELECT TRUNC(AVG(valor_servico),2) AS media_precos_servicos
FROM tbl_servicos;

-- Relatório 2: Soma dos preços dos serviços
SELECT TRUNC(SUM(valor_servico),2) AS total_precos_servicos
FROM tbl_servicos;



-- Relatórios utilizando alguma função de grupo

-- Relatório 1: Contagem de agendamentos por data
SELECT data_agendamento, COUNT(*) AS quantidade_agendamentos
FROM tbl_agendamentos
GROUP BY data_agendamento
ORDER BY data_agendamento;

-- Relatório 2: Contagem de veículos por ano
SELECT ano, COUNT(*) AS quantidade_veiculos
FROM tbl_veiculos
GROUP BY ano
ORDER BY quantidade_veiculos DESC;



-- Relatórios utilizando sub consulta

-- Relatório 1: Veículos com serviços que custam mais que a média de preços
SELECT tabela_a.fabricante, tabela_a.modelo, tabela_a.ano, tabela_b.valor_servico
FROM tbl_veiculos tabela_a
JOIN tbl_servicos tabela_b ON tabela_a.id_cliente = tabela_b.id_servico 
WHERE tabela_b.valor_servico > (SELECT AVG(valor_servico) FROM tbl_servicos);


-- Relatório 2: Clientes que possuem veículos com mais de 3 anos
SELECT tabela_a.nome, tabela_b.ano, tabela_b.modelo
FROM tbl_cadastros tabela_a
JOIN tbl_veiculos tabela_b ON tabela_a.id_cadastro = tabela_b.id_cliente
WHERE EXTRACT(YEAR FROM CURRENT_DATE) - tabela_b.ano > 3;


-- Relatórios utilizando junção de tabelas

-- Relatório 1: Veículos e seus proprietários
SELECT tabela_a.placa, tabela_a.modelo, tabela_a.fabricante, tabela_a.ano, tabela_a.quilometragem_atual, tabela_a.dono, tabela_b.cpf
FROM tbl_veiculos tabela_a
